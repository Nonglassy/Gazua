from copy import deepcopy
starting_threshold = 0.001

for n in range(1,1001):
    threshold = deepcopy(starting_threshold)
    threshold *= n
    su = 1000000
    #처음 백만원으로 계산

    # 8월  i + 1 일
    for i in range(31):
        aa = float(ETH_rate[i])
        bb = float(BTC_rate[i])
        cc = float(ETC_rate[i])
        dd = float(XRP_rate[i])
        ee = float(ADA_rate[i])
        ff = float(QTUM_rate[i])

        maxR = max(aa, bb, cc, dd, ee, ff)

        if maxR >= threshold:
            if maxR == aa:
                su = su + su * float(ETH_pl[i])
            elif maxR == bb:
                su = su + su * float(BTC_pl[i])
            elif maxR == cc:
                su = su + su * float(ETC_pl[i])
            elif maxR == dd:
                su = su + su * float(XRP_pl[i])
            elif maxR == ee:
                su = su + su * float(ADA_pl[i])
            elif maxR == ff:
                su = su + su * float(QTUM_pl[i])

    if su > 1000000:
        print(f"한달 간의 투자 후의 획득 금액:{su}")
        print(f"해당 threshold:{threshold}")